import pandas as pd

# 주어진 데이터를 정의
data = {
    "12m 4d": [7, 2, 0, 1, 9, 33, 58, 95, 93, 72, 67, 66, 67, 72, 72, 74, 85, 100, 88, 58, 44, 34, 22, 13],
    "12m 5d": [7, 3, 0, 2, 10, 35, 60, 94, 96, 74, 68, 70, 69, 72, 73, 78, 84, 100, 91, 60, 48, 34, 23, 13],
    "12m 6d": [7, 2, 0, 1, 9, 33, 58, 95, 93, 72, 67, 66, 67, 72, 72, 74, 85, 100, 88, 58, 44, 34, 22, 13],
    "12m 7d": [9, 3, 0, 1, 10, 36, 62, 100, 99, 80, 73, 76, 74, 75, 75, 82, 91, 100, 85, 65, 48, 41, 29, 16],
    "12m 8d": [9, 5, 0, 2, 10, 34, 59, 91, 91, 74, 69, 70, 73, 72, 75, 81, 91, 100, 88, 60, 51, 40, 29, 19],
}

# 데이터 변환
time_series = []  # 시간 데이터를 저장할 리스트
count_series = []  # 차량 수 데이터를 저장할 리스트

for day, counts in data.items():
    # 날짜를 실제 날짜로 변환 (2024년 12월 기준)
    date = f"2024-{day.replace('m ', '-').replace('d', '')}"
    for hour, count in enumerate(counts):
        # 시간별 데이터 추가
        time_series.append(f"{date} {hour:02d}:00:00")
        count_series.append(count)

# 데이터프레임 생성
df = pd.DataFrame({
    "Time": pd.to_datetime(time_series),  # 문자열을 실제 시간 형식으로 변환
    "Count": count_series
})

# 데이터 확인
print(df.head(10))